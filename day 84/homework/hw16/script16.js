// მათი ჯამი მეტია 100-ზე
let num1 = Number(prompt("შეიყვანე პირველი რიცხვი:"))
let num2 = Number(prompt("შეიყვანე მეორე რიცხვი:"))

if (num1 > 0 && num2 > 0 && (num1 + num2) > 100) {
    alert("ორივე რიცხვი დადებითია და მათი ჯამი მეტია 100-ზე")
} else {
    alert("პირობა არ შესრულდა")
}
