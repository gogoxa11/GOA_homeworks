// მომხმარებელს შემოატანინე რიცხვი და შეამოწმე, არის თუ არა ის 0–10 შუალედში ან 
// 90–100 შუალედში
let num = Number(prompt("შეიყვანე რიცხვი:"));

if ((num >= 0 && num <= 10) || (num >= 90 && num <= 100)) {
    alert("რიცხვი არის 0-10 ან 90-100 შუალედში")
}
else {
    alert("რიცხვი არ არის მითითებულ შუალედებში")
}
