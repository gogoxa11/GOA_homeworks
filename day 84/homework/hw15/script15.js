// (იყოფა 4-ზე და არა 100-ზე, ან იყოფა 400-ზე)
let year = Number(prompt("შეიყვანე წელი:"));

if ((year % 4 === 0 && year % 100 !== 0) || (year % 400 === 0)) {
    alert("წელი ნაკიანწლოვანი არის");
} else {
    alert("წელი ნაკიანწლოვანი არაა");
}
